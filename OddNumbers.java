class OddNumbers   //to print odd numbers from 1 to 100 using do/while loop
{
 public static void main(String[] args)
 {
  int a=1;
  do
    {
     if(a%2==1)
     System.out.println(a);
     a++;
    }
  while(a<=100);
 }
}
    
     
     

   